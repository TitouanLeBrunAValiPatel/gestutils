    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        // TODO add your handling code here:
        listModel.clear();
        System.out.println(jTextField1.getText());
        try {

            ResultSet rs = dao.query("select id, nom, prenom, login, mdp, adresse, cp, ville, dateEmbauche from visiteur where nom LIKE '" + jTextField1.getText() + "%'");
            while (rs.next()) {
                String b = rs.getString("nom");
                String c = rs.getString("prenom");
                String h = rs.getString("ville");
                String i = rs.getString("dateEmbauche");
                visiteur v = new visiteur(rs.getString("id"), rs.getString("nom"), rs.getString("prenom"), rs.getString("login"), rs.getString("mdp"), rs.getString("adresse"), rs.getString("cp"), rs.getString("ville"), rs.getString("dateEmbauche"));
                listModel.addElement(v);
            }
            jList1.setModel(listModel);
        } catch (SQLException e) {
            System.out.println(e);
        }
//System.out.println();

    }//GEN-LAST:event_jTextField1KeyTyped

