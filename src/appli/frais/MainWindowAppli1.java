    private void modifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifActionPerformed

        PreparedStatement preparedStmt;
        try {
            preparedStmt = dao.queryInsert("UPDATE visiteur SET nom= ? ,prenom= ? ,login= ? ,mdp= ? ,adresse= ? ,cp= ? ,ville= ? WHERE id = ? ;");

            preparedStmt.setString(1, nomM.getText());
            preparedStmt.setString(2, prenomM.getText());
            preparedStmt.setString(3, loginM.getText());
            preparedStmt.setString(4, mdpM.getText());
            preparedStmt.setString(5, addressM.getText());
            preparedStmt.setString(6, cpM.getText());
            preparedStmt.setString(7, villeM.getText());
            preparedStmt.setString(8, jList1.getSelectedValue().getId());
            preparedStmt.execute();
            System.out.println(villeM.getText());
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(MainWindowAppli1.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_modifActionPerformed

